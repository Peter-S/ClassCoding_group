package com.company;

public class Grant {

    public String grantName;
    public String grantType;
    public double grantAmmount;
    public double grantMax;
    public double ballance;

    public String getGrantName() {
        return grantName;
    }

    public void setGrantName(String grantName) {
        this.grantName = grantName;
    }

    public String getGrantType() {
        return grantType;
    }

    public void setGrantType(String grantType) {
        this.grantType = grantType;
    }

    public double getGrantAmmount() {
        return grantAmmount;
    }

    public void setGrantAmmount(double grantAmmount) {
        this.grantAmmount = grantAmmount;
    }

    public double getGrantMax() {
        return grantMax;
    }

    public void setGrantMax(double grantMax) {
        this.grantMax = grantMax;
    }

    public double getBallance() {
        return ballance;
    }

    public void setBallance(double ballance) {
        this.ballance = ballance;
    }

}

